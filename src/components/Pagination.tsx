'use client';

interface PaginationProps {
  totalItems: number; // 전체 게시물 개수
  currentPage: number;
  setCurrentPage: (page: number) => void;
  itemsPerPage?: number; // 한 페이지당 보여줄 게시물 수
}

export default function Pagination({
  totalItems,
  currentPage,
  setCurrentPage,
  itemsPerPage = 10,
}: PaginationProps) {
  const totalPages = Math.max(1, Math.ceil(totalItems / itemsPerPage)); // 전체 게시물 개수를 한 페이지당 개수로 나누고 올림 (나머지가 있어도 새로운 페이지가 필요하므로), 최솟값을 1로 보정
  const maxPageNumbers = 5; // 최대 5개 숫자만 보이도록 설정

  // 현재 페이지가 속산 그룹 계산
  const currentGroup = Math.ceil(currentPage / maxPageNumbers); // 현재 페이지가 속한 그룹 (최대 5개까지만 표시)
  const startPage = (currentGroup - 1) * maxPageNumbers + 1; // 현재 그룹에서 가장 작은 페이지 번호
  const endPage = Math.min(startPage + maxPageNumbers - 1, totalPages); // 현재 그룹에서 가장 큰 페이지 번호 (전체 페이지를 넘지 않도록 Math.min() 사용)
  const pageNumbers = Array.from(
    { length: endPage - startPage + 1 },
    (_, i) => startPage + i
  );

  const goToPage = (page: number) => {
    if (page < 1 || page > totalPages) return;
    setCurrentPage(page);
  };

  return (
    <div className="flex items-center gap-2">
      {/* 맨 처음으로 */}
      <button onClick={() => goToPage(1)} disabled={currentPage === 1}>
        <svg
          xmlns="http://www.w3.org/2000/svg"
          width="24"
          height="24"
          viewBox="0 0 24 24"
          fill="none"
        >
          <path
            d="M9.96939 11.4694C9.89965 11.5391 9.84433 11.6218 9.80659 11.7128C9.76885 11.8039 9.74942 11.9015 9.74942 12.0001C9.74942 12.0986 9.76885 12.1962 9.80659 12.2873C9.84433 12.3783 9.89965 12.461 9.96939 12.5307L17.4694 20.0307C17.6101 20.1714 17.801 20.2505 18 20.2505C18.199 20.2505 18.3899 20.1714 18.5306 20.0307C18.6714 19.8899 18.7504 19.6991 18.7504 19.5001C18.7504 19.301 18.6714 19.1102 18.5306 18.9694L11.5603 12.0001L18.5306 5.03068C18.6714 4.88995 18.7504 4.69907 18.7504 4.50005C18.7504 4.30103 18.6714 4.11016 18.5306 3.96943C18.3899 3.8287 18.199 3.74963 18 3.74963C17.801 3.74963 17.6101 3.8287 17.4694 3.96943L9.96939 11.4694ZM6.75001 3.75005C6.94892 3.75005 7.13969 3.82907 7.28034 3.96972C7.42099 4.11037 7.50001 4.30114 7.50001 4.50005V19.5001C7.50001 19.699 7.42099 19.8897 7.28034 20.0304C7.13969 20.171 6.94892 20.2501 6.75001 20.2501C6.5511 20.2501 6.36033 20.171 6.21968 20.0304C6.07903 19.8897 6.00001 19.699 6.00001 19.5001V4.50005C6.00001 4.30114 6.07903 4.11037 6.21968 3.96972C6.36033 3.82907 6.5511 3.75005 6.75001 3.75005Z"
            fill="#E8E8E8"
          />
        </svg>
      </button>

      {/* 이전 */}
      <button onClick={() => goToPage(currentPage - 1)} disabled={currentPage === 1}>
        <svg
          xmlns="http://www.w3.org/2000/svg"
          width="24"
          height="24"
          viewBox="0 0 24 24"
          fill="none"
        >
          <path
            d="M6.96939 12.5307L14.4694 20.0307C14.5391 20.1004 14.6218 20.1556 14.7128 20.1933C14.8039 20.2311 14.9015 20.2505 15 20.2505C15.0986 20.2505 15.1961 20.2311 15.2872 20.1933C15.3782 20.1556 15.461 20.1004 15.5306 20.0307C15.6003 19.961 15.6556 19.8783 15.6933 19.7872C15.731 19.6962 15.7504 19.5986 15.7504 19.5001C15.7504 19.4015 15.731 19.3039 15.6933 19.2129C15.6556 19.1218 15.6003 19.0391 15.5306 18.9694L8.56032 12.0001L15.5306 5.03068C15.6714 4.88995 15.7504 4.69907 15.7504 4.50005C15.7504 4.30103 15.6714 4.11016 15.5306 3.96943C15.3899 3.8287 15.199 3.74963 15 3.74963C14.801 3.74963 14.6101 3.8287 14.4694 3.96943L6.96939 11.4694C6.89965 11.5391 6.84433 11.6218 6.80659 11.7128C6.76885 11.8039 6.74942 11.9015 6.74942 12.0001C6.74942 12.0986 6.76885 12.1962 6.80659 12.2873C6.84433 12.3783 6.89965 12.461 6.96939 12.5307Z"
            fill="#E8E8E8"
          />
        </svg>
      </button>

      {/* 페이지 숫자 */}
      {pageNumbers.map((page) => {
        return (
          <button key={page} onClick={() => goToPage(page)}>
            <div
              className={`px-3 py-2 font-montserrat font-medium ${currentPage === page ? 'text-black' : 'text-[#D2D2D2]'}`}
            >
              {page}
            </div>
          </button>
        );
      })}

      {/* 다음 */}
      <button
        onClick={() => goToPage(currentPage + 1)}
        disabled={currentPage === totalPages}
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          width="24"
          height="24"
          viewBox="0 0 24 24"
          fill="none"
        >
          <path
            d="M17.0306 12.5307L9.53062 20.0307C9.46093 20.1004 9.37821 20.1556 9.28716 20.1933C9.19612 20.2311 9.09854 20.2505 8.99999 20.2505C8.90144 20.2505 8.80386 20.2311 8.71282 20.1933C8.62177 20.1556 8.53905 20.1004 8.46936 20.0307C8.39968 19.961 8.34441 19.8783 8.30669 19.7872C8.26898 19.6962 8.24957 19.5986 8.24957 19.5001C8.24957 19.4015 8.26898 19.3039 8.30669 19.2129C8.34441 19.1218 8.39968 19.0391 8.46936 18.9694L15.4397 12.0001L8.46936 5.03068C8.32863 4.88995 8.24957 4.69907 8.24957 4.50005C8.24957 4.30103 8.32863 4.11016 8.46936 3.96943C8.61009 3.8287 8.80097 3.74963 8.99999 3.74963C9.19901 3.74963 9.38988 3.8287 9.53062 3.96943L17.0306 11.4694C17.1003 11.5391 17.1557 11.6218 17.1934 11.7128C17.2312 11.8039 17.2506 11.9015 17.2506 12.0001C17.2506 12.0986 17.2312 12.1962 17.1934 12.2873C17.1557 12.3783 17.1003 12.461 17.0306 12.5307Z"
            fill="#E8E8E8"
          />
        </svg>
      </button>

      {/* 맨 끝으로 */}
      <button onClick={() => goToPage(totalPages)} disabled={currentPage === totalPages}>
        <svg
          xmlns="http://www.w3.org/2000/svg"
          width="24"
          height="24"
          viewBox="0 0 24 24"
          fill="none"
        >
          <path
            d="M14.0306 11.4694C14.1003 11.5391 14.1557 11.6218 14.1934 11.7128C14.2312 11.8039 14.2506 11.9015 14.2506 12.0001C14.2506 12.0986 14.2312 12.1962 14.1934 12.2873C14.1557 12.3783 14.1003 12.461 14.0306 12.5307L6.53062 20.0307C6.38988 20.1714 6.19901 20.2505 5.99999 20.2505C5.80097 20.2505 5.6101 20.1714 5.46936 20.0307C5.32863 19.8899 5.24957 19.6991 5.24957 19.5001C5.24957 19.301 5.32863 19.1102 5.46936 18.9694L12.4397 12.0001L5.46936 5.03068C5.32863 4.88995 5.24957 4.69907 5.24957 4.50005C5.24957 4.30103 5.32863 4.11016 5.46936 3.96943C5.6101 3.8287 5.80097 3.74963 5.99999 3.74963C6.19901 3.74963 6.38988 3.8287 6.53062 3.96943L14.0306 11.4694ZM17.25 3.75005C17.0511 3.75005 16.8603 3.82907 16.7197 3.96972C16.579 4.11037 16.5 4.30114 16.5 4.50005V19.5001C16.5 19.699 16.579 19.8897 16.7197 20.0304C16.8603 20.171 17.0511 20.2501 17.25 20.2501C17.4489 20.2501 17.6397 20.171 17.7803 20.0304C17.921 19.8897 18 19.699 18 19.5001V4.50005C18 4.30114 17.921 4.11037 17.7803 3.96972C17.6397 3.82907 17.4489 3.75005 17.25 3.75005Z"
            fill="#E8E8E8"
          />
        </svg>
      </button>
    </div>
  );
}
